            <h1 style="background-color: #142b3b; text-align: center; color: white;">
                Agrupación de Contenidos
            </h1>
            <section>
                <div>
                    <h3>P</h3>
                    Define un párrafo.
                    <pre><code class="html">&lt;p&gt;Este es un párrafo&lt;/p&gt;</code></pre>
                    <div>
                        <p>Este es un párrafo</p>
                    </div>
                </div>
                <hr/>
                <div>
                    <h3>Hr</h3>
                    Representa una separación de contenido.
                    <pre>
                            <code class="html">
&lt;p&gt;Este es un párrafo&lt;/p&gt;
&lt;hr/&gt;
&lt;p&gt;Este es otro párrafo&lt;/p&gt;
                            </code>
                    </pre>
                    <div>
                        <p>Este es un párrafo</p>
                        <hr/>
                        <p>Este es otro párrafo</p>
                    </div>
                </div>
                <hr/>
                <div>
                    <h3>Pre</h3>
                    Representa una texto preformateado y el formato es preservado.
                    <pre>
                            <code class="html">
&lt;pre&gt;
    Hola
        Este es un texto preformateado
    Adios
&lt;/pre&gt;
                            </code>
                    </pre>
                    <div>
                        <pre>
    Hola
        Este es un texto con preformateado
    Adios
                        </pre>
                    </div>
                </div>
                <hr/>
                <div>
                    <h3>Ol</h3>
                    Representa una lista ordenada.
                    <pre>
                            <code class="html">
&lt;ol&gt;
    &lt;li&gt;Item uno.&lt;/li&gt;
    &lt;li&gt;Item dos.&lt;/li&gt;
    &lt;li&gt;Item tres.&lt;/li&gt;
    &lt;li&gt;Item cuatro.&lt;/li&gt;
&lt;/ol&gt;
                            </code>
                    </pre>
                    <div>
                        <ol>
                            <li>Item uno.</li>
                            <li>Item dos.</li>
                            <li>Item tres.</li>
                            <li>Item cuatro.</li>
                        </ol>
                    </div>
                </div>
                <hr/>
                <div>
                    <h3>Ul</h3>
                    Representa una lista sin orden.
                    <pre>
                            <code class="html">
&lt;ul&gt;
    &lt;li&gt;Item uno.&lt;/li&gt;
    &lt;li&gt;Item dos.&lt;/li&gt;
    &lt;li&gt;Item tres.&lt;/li&gt;
    &lt;li&gt;Item cuatro.&lt;/li&gt;
&lt;/ul&gt;
                            </code>
                    </pre>
                    <div style="margin-left: 250px; margin-right: 250px">
                        <ul>
                            <li>Item uno.</li>
                            <li>Item dos.</li>
                            <li>Item tres.</li>
                            <li>Item cuatro.</li>
                        </ul>
                    </div>
                </div>
                <hr/>
                <div>
                    <h3>Div</h3>
                    <pre>
                            <code class="html">
&lt;div&gt;
    &lt;p&gt;Este es un párrafo dentro de un div.&lt;/p&gt;
&lt;/div&gt;
                            </code>
                    </pre>
                    <div>
                        <div>
                            <p>Este es un párrafo dentro de un div.</p>
                        </div>
                    </div>
                </div>
            </section>
        </div>
